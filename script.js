function setLanguage(lang) {
  const isArabic = lang === "ar";
  document.documentElement.lang = lang;
  document.documentElement.dir = isArabic ? "rtl" : "ltr";
  document.querySelectorAll("[data-key]").forEach((element) => {
    const key = element.getAttribute("data-key");
    if (translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });

  document.querySelectorAll("[data-placeholder-en]").forEach((input) => {
    const attr = isArabic ? "data-placeholder-ar" : "data-placeholder-en";
    input.placeholder = input.getAttribute(attr);
  });

  renderProducts(lang);
}

function renderProducts(lang) {
  const productGrid = document.getElementById("productGrid");
  productGrid.innerHTML = "";
  products.forEach((product) => {
    const card = document.createElement("article");
    card.className = "product-card";
    card.innerHTML = `
      <img src="${product.image}" alt="${lang === "ar" ? product.nameAr : product.nameEn}" />
      <div class="product-card-content">
        <h4>${lang === "ar" ? product.nameAr : product.nameEn}</h4>
        <p>${lang === "ar" ? product.descriptionAr : product.descriptionEn}</p>
        <div class="product-actions">
          <button class="button button-secondary" type="button">${lang === "ar" ? "عرض المزيد" : "View More"}</button>
        </div>
      </div>
    `;
    productGrid.appendChild(card);
  });
}

function setupGalleryLightbox() {
  const overlay = document.getElementById("lightboxOverlay");
  const overlayImage = document.getElementById("lightboxImage");
  const closeButton = document.getElementById("lightboxClose");

  document.querySelectorAll(".gallery-card").forEach((card) => {
    card.addEventListener("click", () => {
      overlayImage.src = card.dataset.image;
      overlay.classList.add("active");
    });
  });

  closeButton.addEventListener("click", () => overlay.classList.remove("active"));
  overlay.addEventListener("click", (event) => {
    if (event.target === overlay) {
      overlay.classList.remove("active");
    }
  });
}

function setupMobileMenu() {
  const button = document.getElementById("mobileMenuButton");
  const mobileMenu = document.getElementById("mobileMenu");
  button.addEventListener("click", () => {
    mobileMenu.classList.toggle("active");
  });
}

function setupLanguageSwitcher() {
  document.querySelectorAll(".lang-toggle").forEach((button) => {
    button.addEventListener("click", () => {
      document.querySelectorAll(".lang-toggle").forEach((btn) => btn.classList.toggle("active", btn === button));
      setLanguage(button.getAttribute("data-lang"));
    });
  });
}

function setupContactForm() {
  const form = document.getElementById("contactForm");
  if (!form) return;

  form.addEventListener("submit", (event) => {
    event.preventDefault();
    
    // Clear previous error messages
    form.querySelectorAll(".error-message").forEach(msg => msg.style.display = "none");
    const statusDiv = form.querySelector(".form-status");
    statusDiv.style.display = "none";

    // Validate fields
    const name = form.querySelector("input[name='name']");
    const email = form.querySelector("input[name='email']");
    const message = form.querySelector("textarea[name='message']");
    let isValid = true;

    if (!name.value.trim() || name.value.trim().length < 2) {
      name.parentElement.querySelector(".error-message").style.display = "block";
      isValid = false;
    }

    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (!emailRegex.test(email.value)) {
      email.parentElement.querySelector(".error-message").style.display = "block";
      isValid = false;
    }

    if (!message.value.trim() || message.value.trim().length < 10) {
      message.parentElement.querySelector(".error-message").style.display = "block";
      isValid = false;
    }

    if (isValid) {
      statusDiv.textContent = "✓ Message sent successfully! We'll get back to you soon.";
      statusDiv.style.display = "block";
      statusDiv.style.background = "rgba(76, 175, 80, 0.1)";
      statusDiv.style.color = "#2e7d32";
      statusDiv.style.borderLeft = "4px solid #4caf50";
      form.reset();
      setTimeout(() => statusDiv.style.display = "none", 5000);
    }
  });
}

